package javaarraylist;

public class ResultAss9 {
	private int id;
	private int marks;
	
	public ResultAss9() {
		
	}

	public ResultAss9(int id, int marks) {
		super();
		this.id = id;
		this.marks = marks;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}

	@Override
	public String toString() {
		return "ResultAss9 [id=" + id + ", marks=" + marks + "]";
	}
	
	

}
